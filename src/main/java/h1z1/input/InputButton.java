package h1z1.input;

/**
 * A list of buttons that can be pressed. These buttons are
 * input-method-agnostic, and are abstracted away from the
 * actual input device (e.g. keyboard, controller).
 */
public enum InputButton {
    UP,
    DOWN,
    LEFT,
    RIGHT,
    BASIC,
    SPECIAL,
    JUMP,
    BLOCK,
}
